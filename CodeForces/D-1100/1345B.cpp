void solve()
{
    // code goes here...
    int n;
    cin >> n;
    int ans = 0;

    while (n > 1)
    {   //on every turn 2 side cards and 1 base card is needed to make a pyramid so we loop 2 pointer for the same
        for (int i = 2, j = 0; i + j <= n; i += 2, j++)
        {
            n -= i + j;
        }
        ans++;
    }
    cout << ans << "\n";
}
